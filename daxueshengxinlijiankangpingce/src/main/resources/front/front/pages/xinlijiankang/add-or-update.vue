        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {

		/*下拉框*/
			let xinlijiankangTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'xinlijiankang_types',
            }
			let xinlijiankangTypes = await this.$api.page(`dictionary`, xinlijiankangTypesParams);
			this.xinlijiankangTypesOptions = xinlijiankangTypes.data.list
		/*下拉框*/
			let shujuTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shuju_types',
            }
			let shujuTypes = await this.$api.page(`dictionary`, shujuTypesParams);
			this.shujuTypesOptions = shujuTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`xinlijiankang`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.xinlijiankangId){
                this.ruleForm.xinlijiankangId = options.xinlijiankangId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            xinlijiankangTypesChange(e) {
                this.xinlijiankangTypesIndex = e.target.value
                this.ruleForm.xinlijiankangValue = this.xinlijiankangTypesOptions[this.xinlijiankangTypesIndex].indexName
                this.ruleForm.xinlijiankangTypes = this.xinlijiankangTypesOptions[this.xinlijiankangTypesIndex].codeIndex
            },
            shujuTypesChange(e) {
                this.shujuTypesIndex = e.target.value
                this.ruleForm.shujuValue = this.shujuTypesOptions[this.shujuTypesIndex].indexName
                this.ruleForm.shujuTypes = this.shujuTypesOptions[this.shujuTypesIndex].codeIndex
            },


            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },


			xinlijiankangPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.xinlijiankangPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			xinlijiankangVideoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.xinlijiankangVideo = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.xinlijiankangName)) {
                    this.$utils.msg(`心理健康标题不能为空`);
                    return
                }
				if ((!this.ruleForm.xinlijiankangPhoto)) {
                    this.$utils.msg(`心理健康照片不能为空`);
                    return
                }
				if ((!this.ruleForm.shujuTypes)) {
                    this.$utils.msg(`数据类型不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`xinlijiankang`, this.ruleForm);
                } else {
                    await this.$api.save(`xinlijiankang`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
